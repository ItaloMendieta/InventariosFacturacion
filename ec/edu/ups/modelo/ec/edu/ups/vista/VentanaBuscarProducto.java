    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:        
        if (cod != null) {
            Producto c = controladorProducto.BuscarProductoCod(cod);
            System.out.println(" codigo :" + cod + " " + c.getId());
            if (c.getCantidad()<=0) {
                int confirmar = JOptionPane.showConfirmDialog(rootPane, "Seguro que desea eliminar el producto: " + c.getDescripcion());
                System.out.println("00000000000 "+confirmar);
                if (confirmar == 0) {
                    controladorProducto.conexion();
                    controladorProducto.eliminarProducto(c, c.getId());
                    controladorProducto.desconectar();
                    Inventario inve = controladorInventario.BuscarInventarioProducto(c.getId());
                    controladorInventario.conexion();
                    controladorInventario.eliminarInventario(inve, inve.getId());
                    controladorInventario.desconectar();
                    listarProductos();

                }
                if (confirmar == 1 || confirmar == 2) {
                    JOptionPane.showMessageDialog(rootPane, "Eliminacion Cancelada");
                    controladorProducto.desconectar();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se puede eliminar producto " + c.getDescripcion() + " con stock de " + c.getCantidad());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un producto en la tabla");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

