    public void buscarProductos(String producto) {
        try {
            String[] titulo = {"CODIGO", "DESCRIPCION", "PRECIO", "CATEGORIA", "STOCK"};
            modelo = new DefaultTableModel(null, titulo);
            List<Producto> lista = controladorProducto.BuscarProductoLik(producto);
            for (Producto a : lista) {
                Object[] datos = new Object[5];
                datos[0] = a.getCodigo();
                datos[1] = a.getDescripcion();
                datos[2] = a.getCategoria().getDescripcion();
                datos[3] = a.getPrecio();
                datos[4] = a.getCantidad();

                modelo.addRow(datos);
            }
            jTable1.setModel(modelo);
        } catch (Exception ex) {
        }

    }

    public void listarProductos() {

        jTable1.getModel();
        int ab = jTable1.getRowCount() - 1;
        for (int i = ab; i >= 0; i--) {
            model1.removeRow(model1.getRowCount() - 1);
        }

        List<Producto> lista = controladorProducto.listar();
        for (Producto a : lista) {
            Object[] datos = new Object[15];
            datos[0] = a.getCodigo();
            datos[1] = a.getDescripcion();
            datos[2] = a.getCategoria().getDescripcion();
            datos[3] = a.getPrecio();
            datos[4] = a.getCantidad();
            datos[5] = a.getProveedor().getNombre();
            model1.addRow(datos);
        }
        jTable1.setModel(model1);
    }

